#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char	*ps1;
	unsigned char	*ps2;
	size_t			i;

	ps1 = (unsigned char *)s1;
	ps2 = (unsigned char *)s2;
	i = 0;
	while (ps1[i] && i < n && ps1[i] == ps2[i] && n)
		i++;
	if (i == n)
		return (0);
	return (ps1[i] - ps2[i]);
}
